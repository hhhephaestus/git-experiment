	public int neighbor(int row, int col)//该细胞周围活细胞数量
	{
		int i, j;
		int count = 0;
		for (i=row-1;i<row+2;i++)
		{
			for(j=col-1;j<col+2;j++)
			{
				if(row==0||col==0||row==41||col==36)
					continue;
				if(map[i][j]==1)
					count+=1;
			}
		}
		if(map[row][col]==1)
			count-=1;
		return count;
	}
	
	public void update_map()//更新至new_map
	{
		for(int i=1;i<map.length-1;i++)
			for(int j=1;j<map[i].length-1;j++)
			{
				int n = neighbor(i,j);
				//System.out.println(n);
				if(n!=2&&n!=3)
					new_map[i][j] = 0;
				else if(n==3)
					new_map[i][j] = 1;
				else
					new_map[i][j] = map[i][j];			
			}
	}
	
	public void copy_map()//将new_map复制到map以进行下一轮判断
	{
		for(int i=0;i<map.length;i++)
			for(int j=0;j<map[i].length;j++)
				map[i][j]=new_map[i][j];
	}
	
